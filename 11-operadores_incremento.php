<?php 

//ejemplo incremento
$numero_1 = 10;
$numero_2 = 10;

//pre-incremento
echo ++$numero_1 . "<br><br>";
//post-incremento
echo $numero_2++ . "<br>";
echo $numero_2;
echo "<br><br>";

//ejemplo decremento
$numero_3 = 10;
$numero_4 = 10;
echo --$numero_3 . "<br><br>";
//post-incremento
echo $numero_4-- . "<br>";
echo $numero_4;
?>