<?php 
/*
tipos de dato booleanos
cuando se declara un boolenao como true este dara como resultado 1
en cambio si se daclara como false este ni mostrara ningun resultado
*/
$boleano = true;
echo $boleano . "<br>";
var_dump($boleano);
echo "<br>";
/*
tipos de dato enteros o int
son los numeros reales que conocemos
incluyendo los negativos
*/
