$entero2 = -5;
echo $entero1 . "<br>" . $entero2 . "<br>";
var_dump($entero1);
var_dump($entero2);
echo "<br>";

/*
tipos de dato flotantes o dobles
al igual que los enteros muestran un valor numerico con la diferencia
de que este puede incluir valores decimales
*/
